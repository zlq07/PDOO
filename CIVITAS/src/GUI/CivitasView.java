    public OperacionInmobiliaria elegirOperacion() {
        GestionarDialog a= new GestionarDialog(this);
        OperacionInmobiliaria Oi;
        int guardar=a.getGestion();
        //return (OperacionInmobiliaria.values()[guardar]);
        switch(guardar){
            case 0:
                Oi=OperacionInmobiliaria.CONSTRUIR_CASA;
            break;
            case 1:
                Oi=OperacionInmobiliaria.CONSTRUIR_HOTEL;
            break;
            default:
                Oi=OperacionInmobiliaria.TERMINAR;
            break;
                
                
        }
        return Oi;
        
    }

