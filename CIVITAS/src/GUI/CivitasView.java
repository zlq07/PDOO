    public void actualiza(){
        jugadorPanel1.setJugador(juego.getJugadorActual());
        
        TextoCasilla.setText(juego.getCasillaActual().toString());
        Ranking.setVisible(false);
        TextoRanking.setVisible(false);
        
        if(juego.finalDelJuego()){
            Ranking.setVisible(true);
            TextoRanking.setVisible(true);
            
            String texto = "";
            ArrayList <Jugador> lista= juego.ranking();
            for(int i=0;i<lista.size();i++){
                texto = texto + lista.get(i).toString() + "\n";
            }
            TextoRanking.setText(texto);
            
        }
        pack();
        repaint();
        revalidate();
    }
    
